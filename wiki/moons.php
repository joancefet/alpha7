    <span class="tekst"> <a  style="text-decoration:none;" name="sovety">Buildings moon</a></span>
    <br />
    <hr />
    <br />

<p>
Теперь перейдем к застройке луны. Тут очень важно правильно начать.. <br>
Первое, что мы делаем, это строим лунные базы, можно примерно до 10-15 уровня. Затем строим Фабрики роботов 10 уровня для того, чтобы открыть Фабрику Нанитов (да, да, тут Нанитки можно строить и на луне). <br>
Фабрику нанитов я советую поднимать примерно до 8-9 уровня. Объясню почему: во-первых, 8-й уровень нужен для строительства Среднего конвейера, который поможет нам защитить нашу луну от сноса. Во-вторых, в будущем при строительстве построек, время на их строительство будет достигать часа и более, поэтому важно с самого начала поставить Нанитки для ускорения застройки. <br>
<br>
В общем, Нанитки поставили, что дальше? Если полей осталось мало, есть два пути: построить еще лунных баз, либо же снести фабрику роботов с 10 до 2 уровня. Многие думают, что снеся Фабрику роботов мы тем самым удаляем и Фабрики нанитов. НЕТ, это не так, нанитки остаются на луне и продолжают работу.
 Как только поля освободились, строим верфь, к примеру, 14 уровня для конвейеров. Можно конечно построить верфь и 20-го уровня для тяжелой обороны, но не суть важно, смотрите сами по ситуации… <br>
<br>
Как только верфь 14 уровня построена, открывается возможность построить два конвеера: легкий и средний. Сразу советую сделать конвейеры примерно 10 уровня. Но опять же смотрите наперед.. если вы собираетесь активно ставить оборону на луне, то стройте конвейеры выше. 
Идем дальше.. сразу нужно построить склад альянса, чтобы в случае чего ваши соаловцы или друзья могли защитить вашу луну с помощью своего флота (миссия Держаться)
Также сразу лучше построить Телепорт и Сенсорную фалангу. 
Хранилища на луне строить не надо! В них нет никакого смысла, ресурсы никуда не пропадут. <br>
<br>
Теперь может случиться такая ситуация, что лунная база слишком дорога, а полей мало. Тогда нам на помощь приходит ТМ. <p /> 

<ol>
Да, да, поля можно покупать и с помощью ТМ. Для этого необходимо проделать следующие операции:
<li>1.	Зайдите через меню слева в Дилеры. 
<li>2.	В меню Дилеры найдите Планетарий, откроется окно: 


 
             
             <center><img style="border-radius:10px" width="600px" src="html/images/4/1.png" ><br /></center>




<li>3.	Впишите кол-во полей, которые вы хотите добавить и смотрите цену в ТМ. Если устраивает – покупайте. <br />
Отмечу сразу, что цена полей за ТМ увеличивается по мере использования функции покупки полей за ТМ.
</ol>

<br />
    <span class="tekst"> <a  style="text-decoration:none;" name="sovety">Коллайдер</a></span>
    <br />
    <hr />
    <br />

Коллайдер – особая постройка на луне, которая производит ТМ. Коллайдер строится за АМ, но он никаким образом не занимает поля. Это постройка другого типа. Для постройки коллайдера зайдите в обзор, при этом находясь на луне.
  
             
             <center><img style="border-radius:10px"  height="170" src="html/images/4/2.png"><br /></center>



Справа будет видно слово Коллайдер, нажмите на него. Откроется окно: 

             <center><img style="border-radius:10px"  height="160" src="html/images/4/3.png" ><br /></center>


 Там будет написано, сколько ТМ будет производить следующий уровень коллайдера, стоимость улучшения коллайдера. Чтобы посмотреть выработку ТМ, просто нажмите на картинку Коллайдера.
Коллайдер стройте на свой страх и риск! Его могут легко снести ваши враги и тогда, потраченная на коллайдер АМ, будет потеряна навсегда. Цена улучшения коллайдера увеличивается примерно на 30% с каждым уровнем.. <p>

<br />
    <span class="tekst"> <a  style="text-decoration:none;" name="sovety">Снос луны</a></span>
    <br />
    <hr />
    <br />
Теперь снос луны. Для сноса нужен особенный корабль Звезда Смерти (ЗС), а для ЗС нужен офицер Уничтожитель. Не забудьте про офицера. Я сам однажды построил ЗС, хотел снести луну, а офицера то не нанял, так что сразу купите офицера, а потом уже стройте ЗС. <br><br>
Для того, чтобы заставить игрока задуматься, нужно ли сносить луну противнику и рисковать соим флотом, достаточное кол-во звезд для уничтожения луны сделали на уровне 50 тысяч ЗС. Вы должны понимать, что риск уничтожения звезд достаточно велик. При разлете звезд на куски, разлетается и ваш флот, который летел с ЗС.
<br><br>
На шанс  уничтожения звезд повлиять нельзя, система сама занимается этим шансом, а также, для баланса создания лун и их уничтожения, скорость ЗС заметно уменьшили, так что полет до луны противника может занимать как 15 мин, так и более часа. Естественно, что за это время противник может зайти в игру и помешать вашим звездам, например, выставив сильный флот на орбите луны, поэтому желательно не оставлять луноснос без присмотра. <p>
В случае, если на луне есть оборона, обязательно воспользуйтесь Симулятором боя для того, чтобы узнать какой минимальный флот нужен для уничтожения обороны противника, ведь вам не выгодно тратить целый час на бессмысленный полет ЗС в ничью… <br><br> 

Теперь что же может помочь вам уменьшить шанс на снос вашей луны:<br />
<ol>
<li>1.	Оборона, для пролома которой нужно отправлять большой флот. 
<li>2.	Премиум-функция “Защита лун” (увеличивает кол-во ЗС в “X” раз, необходимых для сноса)
<li>3.	Лунная база также на несколько процентов уменьшает шанс противника на снос вашей луны.
</ol>
	<br><br>
Скажу сразу, что самым простым является строительство обороны.. причем акцент стоит делать на постройки с мощными щитами и те, против которых у флота нет скорострела (плазмы, ионные орудия, пушки гауса и тяжелая оборона).

