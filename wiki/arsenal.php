	<h1>Арсенал</h1>

Арсенал – пункт меню, в котором можно устанавливать апгрейды, покупать и продавать их. <br />
Апгрейды вы можете находить в эскпедиции, либо покупать их за АМ или ТМ в арсенале. Другими словами – арсенал – это рынок апгрейдов. <p>
<ul>
<li> Каждый апгрейд при удачной установки добавляет 5% к исследуемой характеристике.
<li>При  неудачной установке апгрейда, бонус характеристики может упасть от 0 до 25%
<li> Шанс на неудачную установку апгрейда повышается с ростом процентов исследуемой характеристики. 
<li> Максимальная цена на апгрейд – 30 000 АМ или 750 000 ТМ
</ul>

<br />
