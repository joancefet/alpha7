<br />
    <span class="tekst"> <a  style="text-decoration:none;" name="sovety">Как купить апгрейд</a></span>
    <br />
    <hr />
    <br />

Действия аналогичны  продаже, за исключением того, что нужно сначала нажать на кнопку <b>Купить</b>.<p />
В окне покупки надо также выбрать единицы покупки (АМ или ТМ), кол-во, а вот с ценой сложнее. 
Цена, которую вы вписываете, является верхним пределом поиска апгрейда. <br />То есть, если вы вписываете макс. цену 2000 АМ, то система подыщет вам самый дешевый апгрейд по цене до 2000 АМ. <p />
Тут желательно начинать поиски с минимальных цен. <br />Если быть точнее, то если вы хотите купить апгрейд и у вас, к примеру, 1000 АМ, то начинайте поиски с указанием максимальной цены сначала в 100 АМ, затем 200 и так далее. <p>



