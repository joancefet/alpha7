	<h1>Оборона</h1>
          
          
       
Оборона – средство защиты как планеты, так и луны от нападений. <br />
На данном проекте оборона делится на три класса: <ul>

<li>легкая: РУ (ракетная установка), ЛЛ (легкий лазер) и ТЛ (тяжелый лазер);
<li>средняя: Ионное орудие, Пушка Гауса и Плазменное орудие
<li>тяжелая: Гравитонное орудие, Орбитальная база, Лептонная пушка, Протонное орудие, Каньен, Квантовая пушка <p> </ul>
Все эти классы различаются силой вооружений, типом щитов и брони. 
<p>

