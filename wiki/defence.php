На скорость строительства обороны, как и флота влияют конвейеры.  <p>
<b>Легкий конвейер:</b> Ракетная установка, Лёгкий лазер, Тяжёлый лазер. <br>
<b>Средний конвейер:</b> Пушка Гаусса, Ионное орудие, Плазменное орудие <br>
<b>Тяжёлый конвейер:</b> Гравитонное орудие, Лептонная пушка, Протонное орудие, Каньен, Квантовая пушка <p>

При строительстве обороны на планете или луне, стоит строить оборону всех классов, начиная с Ракетных установок. То есть. сначала легкую оборону, которая в основном будет работать против основного мяса среди флота – Легких истребителей. <p>
<b>10 кк Ракетных установок, 50 кк Легких лазеров, 20 кк Тяжелых лазеров  </b>    (кк) – миллионы; <p>              
Затем идет основное мясо – средняя оборона. Из-за мощных щитов, наиболее предпочтительным является Ионное орудие, затем также не стоит пренебрегать орудиями, которые не имеют  скорострела (Плазмы и Гаусы) <p>
<b>5 кк Гаусов, 20 Ионных орудий и 2,5 кк плазм.</b> <p>
Под конец у нас идут тяжи. Они отличаются высокой огневой мощью и тяжелыми щитами. <p> 
<b>5 к Гравитонных орудий, 15 к Лептонных пушек, 2,5к Протонных орудий, 2к Каньенов, 1к Квантовых</b> <p>
Такая оборона с лихва подойдет для сохранения ресурсов, так как на такую обу с выгодой слетать будет очень тяжело. 

