<ul>
<li><strong>focus</strong> – increases attack power ships, or to be more precise, the focus reduces the number of ships that when calculating combat resist ship.
<li><strong>double Attack</strong> – increases the chance of doubling attack ships.
<li><strong>chain reaction</strong> – increases the chance of the emergence of the so-called chain reaction, perhaps, is the most dangerous skill for both the attacker and the defender.
<li><strong>strengthening explosion</strong> – increase the maximum number of units that can destroy a chain reaction
<li><strong>Academy of Sciences</strong> – skill that raises your level of research Intergalactic Research Network. This study allows to combine research laboratories on all your colonies, while reducing the time of research.
