	<h1>Statistics</h1>



           Here's a view of statistics on our project.
      <div align="center"> <img style="border-radius:10px"  height="160" src="html/images/24/1.png" onclick="changeSizeImage(this)"> </div>
            As in other games, There are differents ways to display the stats. Statistics can be sorted by the following indicators: Buildings, Research, Defense, Fleets, Points (sum). <p>
             
              To view the statistics, click on the icon in the upper menu:  <img src="http://astro-mania.com/styles/images/iconav/stats.png" />
             <p>
            Statistical indicators are updated every 4 hours, ie night going 6 updates. During the upgrade points each player translated by all indicators, that is, for example, if the player has bought the fleet after the update, then points the fleet affect the player only after the next update. <p>
              By clicking on the nick of any player in the statistics, you can see the statistics of his battles, and much more. <p>
             <br />
    <span class="tekst"> <a  style="text-decoration:none;" name="sovety">Points</a></span>
    <br />
    <hr />
    <br />

            Here at first glance, everything is simple, but I must say that has its secrets. In other words, for every 1 million spent resources, you get 1 point in the statistics. For research and construction, points are awarded according to the following system: 1 point - 500 000 resources. <p>
              That is, if you want to grow rapidly on statistics, then focus specifically on research and (or) mines.
             <p>
            Fleets and defense bring points as usual, ie 1 point = 1 million resources. <br />
              By the way, when buying for DM something, Points are also added. <p>
                          <br />
    <span class="tekst"> <a  style="text-decoration:none;" name="sovety">Noob protection</a></span>
    <br />
    <hr />
    <br />
             On this project protection works with the x5 conditions.
              <P>
              Explain what it means. This means exactly wich player you are for another players, for example, with 100 million points, players can attack other players in the range from 20 kk points to 500 kk points. Noob protections take only the total points amount in count, Statistics for fleet and other indicators by themselves are not important.
             
   
