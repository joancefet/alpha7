	<h1>resources</h1>
           Resources are the main game unit. There are three types of resources: metal, crystal and deuterium.<br />
           
Briefly explain the purpose of each type of:<br />
<ul style="margin-left:5px">
<li> Metal - used in almost all buildings and all fleets
<li> Crystal - as well as the metal is used almost everywhere.
<li> Deuterium - undoubtedly the most valuable resource of all. It is used as fuel fleet, and is also present in the structure of the heavy ships and defense. However, the construction of mines does not require deuterium, at all.
</ul>
<br />
<br />
You can produce ressource your self and/or steal it from other players. It depends on your playstyle. but , as a rule, if you steal something from someone , be prepared that he may come back to you. so there you have to decide for yourself which way you get the resources easier. <br />you can also to send the fleet on an expedition, and have a chance to bring home some ressource. <P />
and as last way, but it is based more on luck : I'm talking about someone else collecting the debris field . For example there was a battle , and none of the players have recycled the debris, then you can send yours and try to steal it .


