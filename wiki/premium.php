Now more about the function of each premium account: <p>

<ul>
<li> <strong> rate of resource extraction </strong> - a service that increases the amount of resources produced per unit time. This is the most popular service among the pre,iu, functions . For example, say that when buying a bonus of 200 % , production increased by about 2 times.
<li> <strong> Storage capacity </strong> - is also a very useful feature that is worth taking with a premium resource extraction , it is not always possible to build enough storage with your avaible fields.
<li> <strong> speed construction and research </strong> - increases the speed of construction of mines and buildings , as well as the speed of research. Studies that have already started does not change. Note: does not affect the speed of construction for fleets and defense.
<li> <strong> Building and research queue</strong> - adds a line to the buildings and research. Remember that basicly, buildings is set to 3, and research to 1. A very useful service when you leave the study of construction or build time of your absence. This don't work on the shipyard queu !
<li> <strong> "Bonus" button </strong>. Bonus buttons appears about every 3-4 hours. This will reward you with higher values.
<li> <strong> Bonus "Expeditions" </strong>. Expeditions can give the player several events : This function increases the chance of finding an upgrade, increase the size of the fleets, resources, or DM found, as well as reduces the chance of the loss of the fleet and the battle with the pirates.
<li> <strong> Number "Expeditions" </strong>. Increases the number of fleets, which can be simultaneously send for Expeditions
<li> <strong> Protection moon </strong>. Reduces the chance of destroying your moon , and also increases the number of RIPS required for demolition
<li> <strong> Number earn experience </strong>. Increases the number of points the alliance, earned for the fight and also reduces the time required for the next level.
<li> <strong> Alliance Bank </strong>. Increases the maximum number of resources that you can put in the bank alliance.</ul><p>
<font size="3"> <b>Important: </b> when you have already bought the Premium account, it can not be canceled, only cancel all and buy again. </font>
  
            
